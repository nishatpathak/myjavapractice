package loopPractice;

public class ForLoopPractice3 {
    public static void main(String[] args) {
        for(int t=5;t<=50;t++){
            for(int i=5;i<50;i++){
                System.out.println(t+" "+i);
            }
        }
        for(int i=1;i<=5;i++)
        {
            for(int j=2;j<=6;j++){
                System.out.println(i+" "+j);
            }
        }
    }
}
