package loopPractice;

public class WhilePractice {
    public static void main(String[] args) {
        int balance = 1;
        while (balance <= 10) {
            System.out.println(balance);
            balance++;
        }
        int balance1=30;
        while (balance1>=10){
            System.out.println(balance1);
            balance1--;
        }
    }
    }

