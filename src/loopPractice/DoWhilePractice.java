package loopPractice;

public class DoWhilePractice {
    public static void main(String[] args) {
        int number=10;
        do{
            System.out.println(number);
            number++;}
            while(number<=20);
            int withdraw=20;
            do{
                System.out.println(withdraw);
                withdraw--;}
            while (withdraw<=15);
            }

    }

