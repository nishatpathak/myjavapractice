package loopPractice;

public class SquareNumber {
    public static void squareRoot() {
        for (int i = 5; i <= 20; i++) {
            int b = i * i;
            System.out.println("square of number" + " " + "=" + " " + b);
        }
    }



    public static void main(String[] args) {
        squareRoot();
    }
}
