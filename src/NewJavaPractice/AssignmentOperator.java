package NewJavaPractice;

public class AssignmentOperator {
    public static void main(String[] args) {
        int z=40;
        z+=20;
        //60//
        System.out.println(z);
        int t=50;
        t-=10;
        //40//
        System.out.println(t);
        int y=100;
        y*=2;
        //200//
        System.out.println(y);
        int j=30;
        j/=15;
        //2//
        System.out.println(j);
        int h=90;
        h%=7;
        //6//
        System.out.println(h);

    }
}
