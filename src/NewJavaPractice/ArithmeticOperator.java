package NewJavaPractice;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int apple = 10;
        int banana = 20;
        //30//
        System.out.println(apple+banana);
        byte strawberry=40;
        byte blueberry=30;
        //10//
        System.out.println(strawberry-blueberry);
        int  tree=5;
        int plants=40;
        //200//
        System.out.println(tree*plants);
        float girl= 70;
        float boy= 7;
        //10//
        System.out.println(girl/boy);
        int x=55;
        int y= 7;
        //6//
        System.out.println(x%y);
        //increment//
        int cow= 40;
        int sheep= ++cow;
        //41//
        System.out.println(sheep);

        //decrement//
        int dog=90;
        int cat=--dog;
        //89//
        System.out.println(cat);
        //postfix//
        int a=10;
        System.out.println(a);
        ++a;
        System.out.println(a++);
        //11//
        int g=5;
        System.out.println(g);
        --g;
        System.out.println(g--);
        //4//





    }
}
